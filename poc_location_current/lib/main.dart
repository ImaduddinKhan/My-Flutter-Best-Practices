            if (_currentAddress != null) Text(_currentAddress!),

            // ignore: deprecated_member_use
            FlatButton(
              child: const Text("Get location"),
              onPressed: () {
                // Get location here
                _getCurrentLocation();
              },
            ),
          ],
        ),
      ),
    );
  }
